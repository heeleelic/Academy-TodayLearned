package day1228;

public class OperTest_05 {

	public static void main(String[] args) {
		//대입 연산자
		//'누적'의 개념! 
		
		int a,b,c,d,e;
		a=b=c=d=e=10;
		
		a+=2; //12
		b-=3; //7
		c*=3; //30
		d/=3; //3
		e%=3; //1
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println("d="+d);
		System.out.println("e="+e);
		
		

	}

}
